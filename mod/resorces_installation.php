				<br/><p style="text-align: left; font-size: 20px; margin-left:10px; color: #3F5EAB; ">Without Air Brakes</p>
				<p style="text-align: justify; font-size: 18px; margin-left:10px;">
					Place horn in position desired on top of the cab. MARK mounting holes. DRILL mounting holes with 5/16" drill and air inlet hole with ?" drill. ATTACH horns to the top of the cab with ?" bolts. SCREW 1630 valve into air inlet at base of horn using 1/8" NPT close nipple. TURN valve so control handle points to front of cab. INSTALL air tank firmly on frame at rear of cab. CONNECT tubing to base of the air valve and air tank with fittings. PERMATEX all fittings to prevent leakage. ATTACH small chain to control lever of valve. INSERT air fill valve in end of tank. FILL at service station. BLOW HORN
				</p>
