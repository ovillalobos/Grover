				<p style="text-align: justify; font-size: 18px; margin-left:10px;">
					CONSULT truck mfg for location of junction box. PLACE horns in position desired on top of the cab. MARK mounting holes. DRILL mounting holes with 5/16" drill and air inlet hole with ?" drill. ATTACH horns to the top of cab with ?" bolts. SCREW 1630 valve into air inlet at base of horn using 1/8" NPT close nipple. TURN valve so control handle points to front of cab. INSTALL air tank firmly on frame at rear of cab. CONNECT tubing to base of the air valve and air brake junction box with fittings. PERMATEX all fittings to prevent leakage. ATTACH small chain or strap to control level of valve. Truktone air horn models do not include front pedestal. BLOW HORN
